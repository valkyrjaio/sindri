namespace Sindri\Tests\Unit\Generator\Container;

use Sindri\Generator\Container\DataFileGenerator;
use Sindri\Generator\Enum\GenerateStatus;
use Sindri\Tests\Unit\Abstract\TestCase;
use Valkyrja\Application\Directory\Directory;
use Valkyrja\Container\Data\ContainerData;

final class DataFileGeneratorTest extends TestCase
{
    public function testGenerateFile(): void
    {
        $directory  = Directory::storagePath();
        $className  = 'ContainerDataTestData';
        $filePath   = "$directory/$className.php";
        $data       = new ContainerData();
        $generator  = new DataFileGenerator(
            directory: $directory,
            data: $data,
            namespace: 'App',
            className: $className
        );
        $results   = $generator->generateFile();

        self::assertSame(GenerateStatus::SUCCESS, $results);
        self::assertSame($generator->generateFileContents(), @file_get_contents($filePath));

        @unlink($filePath);
    }

    public function testGenerateFileContents(): void
    {
        $directory  = Directory::storagePath();
        $className  = 'ContainerDataTestData';
        $namespace  = 'App';
        $data       = new ContainerData();
        $generator  = new DataFileGenerator(
            directory: $directory,
            data: $data,
            namespace: $namespace,
            className: $className
        );
        $contents  = $generator->generateFileContents();

        $expected = <<<PHP
            <?php

            declare(strict_types=1);

            // This file was automatically generated by the Valkyrja Framework.

            namespace $namespace;

            use Valkyrja\Container\Data\ContainerData;

            final readonly class $className extends ContainerData
            {
                public function __construct()
                {
                    parent::__construct(
                        aliases: array (
            ),
            deferred: array (
            ),
            deferredCallback: array (
            ),
            services: array (
            ),
            singletons: array (
            ),
            providers: array (
            )
                    );
                }
            }

            PHP;

        self::assertNotEmpty($contents);
        self::assertSame($expected, $contents);
    }

    public function testGenerateClassContents(): void
    {
        $directory  = Directory::storagePath();
        $className  = 'ContainerDataTestData';
        $data       = new ContainerData();
        $generator  = new DataFileGenerator(
            directory: $directory,
            data: $data,
            namespace: 'App',
            className: $className
        );
        $contents  = $generator->generateClassContents();

        $dataNamespace = ContainerData::class;

        // phpcs:disable
        $expected = <<<PHP
            new \\$dataNamespace(
                aliases: array (
            ),
            deferred: array (
            ),
            deferredCallback: array (
            ),
            services: array (
            ),
            singletons: array (
            ),
            providers: array (
            )
            )
            PHP;
        // phpcs:enable

        self::assertNotEmpty($contents);
        self::assertSame($expected, $contents);
    }
}
