namespace Sindri\Tests\Unit\Generator\Cli;

use Sindri\Generator\Cli\DataFileGenerator;
use Sindri\Generator\Enum\GenerateStatus;
use Sindri\Tests\Unit\Abstract\TestCase;
use Valkyrja\Application\Directory\Directory;
use Valkyrja\Cli\Interaction\Output\Contract\OutputContract;
use Valkyrja\Cli\Interaction\Output\Output;
use Valkyrja\Cli\Routing\Data\CliRoutingData;
use Valkyrja\Cli\Routing\Data\Contract\RouteContract;
use Valkyrja\Cli\Routing\Data\Route;

final class DataFileGeneratorTest extends TestCase
{
    /**
     * Handler for the command.
     */
    public static function handler(): OutputContract
    {
        return new Output();
    }

    public function testGenerateFile(): void
    {
        $directory  = Directory::storagePath();
        $className  = 'CliDataTestRoutingData';
        $filePath   = "$directory/$className.php";
        $data       = new CliRoutingData();
        $generator  = new DataFileGenerator(
            directory: $directory,
            data: $data,
            namespace: 'App',
            className: $className
        );
        $results   = $generator->generateFile();

        self::assertSame(GenerateStatus::SUCCESS, $results);
        self::assertSame($generator->generateFileContents(), @file_get_contents($filePath));

        @unlink($filePath);
    }

    public function testGenerateFileContents(): void
    {
        $directory  = Directory::storagePath();
        $className  = 'CliDataTestRoutingData';
        $namespace  = 'App';
        $data       = new CliRoutingData();
        $generator  = new DataFileGenerator(
            directory: $directory,
            data: $data,
            namespace: $namespace,
            className: $className
        );
        $contents  = $generator->generateFileContents();

        $expected = <<<PHP
            <?php

            declare(strict_types=1);

            // This file was automatically generated by the Valkyrja Framework.

            namespace $namespace;

            use Valkyrja\Cli\Routing\Data\CliRoutingData;

            final readonly class $className extends CliRoutingData
            {
                public function __construct()
                {
                    parent::__construct(
                        routes: [
                
            ],
                    );
                }
            }

            PHP;

        self::assertNotEmpty($contents);
        self::assertSame($expected, $contents);
    }

    public function testGenerateClassContents(): void
    {
        $directory  = Directory::storagePath();
        $className  = 'CliDataTestRoutingData';
        $data       = new CliRoutingData();
        $generator  = new DataFileGenerator(
            directory: $directory,
            data: $data,
            namespace: 'App',
            className: $className
        );
        $contents  = $generator->generateClassContents();

        $dataNamespace = CliRoutingData::class;

        // phpcs:disable
        $expected = <<<PHP
            new \\$dataNamespace(
                routes: [
                
            ],
            )
            PHP;
        // phpcs:enable

        self::assertNotEmpty($contents);
        self::assertSame($expected, $contents);
    }

    public function testGenerateClassContentsWithRoute(): void
    {
        $data      = new CliRoutingData(
            routes: [
                'route' => static fn (): RouteContract => new Route(
                    name: 'test',
                    description: 'description',
                    handler: [self::class, 'handler']
