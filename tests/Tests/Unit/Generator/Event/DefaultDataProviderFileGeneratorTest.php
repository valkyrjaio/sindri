namespace Sindri\Tests\Unit\Generator\Event;

use Sindri\Generator\Enum\GenerateStatus;
use Sindri\Generator\Event\DefaultDataProviderFileGenerator;
use Sindri\Tests\Unit\Abstract\TestCase;
use Valkyrja\Application\Directory\Directory;

final class DefaultDataProviderFileGeneratorTest extends TestCase
{
    public function testGenerateFile(): void
    {
        $directory  = Directory::storagePath();
        $className  = 'EventDefaultDataTestDataProvider';
        $filePath   = "$directory/$className.php";
        $generator  = new DefaultDataProviderFileGenerator(
            directory: $directory,
            namespace: 'App',
            className: $className
        );
        $results   = $generator->generateFile();

        self::assertSame(GenerateStatus::SUCCESS, $results);
        self::assertSame($generator->generateFileContents(), @file_get_contents($filePath));

        @unlink($filePath);
    }

    public function testGenerateFileContents(): void
    {
        $directory  = Directory::storagePath();
        $className  = 'EventDefaultDataTestDataProvider';
        $namespace  = 'App';
        $generator  = new DefaultDataProviderFileGenerator(
            directory: $directory,
            namespace: $namespace,
            className: $className
        );
        $contents  = $generator->generateFileContents();

        $expected = <<<PHP
            <?php

            declare(strict_types=1);

            // This file was automatically generated by the Valkyrja Framework.

            namespace $namespace;

            use Override;
            use Valkyrja\Event\Data\EventData;
            use Valkyrja\Container\Provider\Contract\ServiceProviderContract;
            use Valkyrja\Container\Manager\Contract\ContainerContract;
            use Valkyrja\Event\Provider\EventServiceProvider;

            final class EventDefaultDataTestDataProvider implements ServiceProviderContract
            {
                /**
                 * @inheritDoc
                 */
                #[Override]
                public static function publishers(): array
                {
                    return [
                        EventData::class => [self::class, 'publishData'],
                    ];
                }

                /**
                 * @inheritDoc
                 */
                #[Override]
                public static function provides(): array
                {
                    return [
                        EventData::class,
                    ];
                }

                /**
                 * Publish the service.
                 */
                public static function publishData(ContainerContract \$container): void
                {
                    EventServiceProvider::publishData(\$container);
                }
            }

            PHP;

        self::assertNotEmpty($contents);
        self::assertSame($expected, $contents);
    }
}
